// Utility functions
export const getThemeColor = (color, shade = 500, isDark = false) => {
  if (isDark && theme.darkColors[color]) {
    return theme.darkColors[color];
  }
  return theme.colors[color]?.[shade] || color;
};

export const getResponsiveValue = (values, breakpoint = 'md') => {
  const breakpoints = ['sm', 'md', 'lg', 'xl', '2xl'];
  const currentIndex = breakpoints.indexOf(breakpoint);
  
  if (Array.isArray(values)) {
    return values[currentIndex] || values[values.length - 1];
  }
  
  return values;
};

